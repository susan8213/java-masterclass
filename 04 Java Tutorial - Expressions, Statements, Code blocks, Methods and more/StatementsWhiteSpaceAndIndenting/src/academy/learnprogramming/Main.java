package academy.learnprogramming;

/*
    Statements
        use whitespace and indenting to make you easy to read the code logic.
 */

public class Main {

    public static void main(String[] args) {

        // 1 Statement in one line
        int myVariable = 50;
        myVariable++;
        myVariable--;
        System.out.println("This is a test.");

        // 1 Statement can be written in many lines.
        System.out.println("This is" +
                "another" +
                "still more.");

        // Statements cane be written in one line, bit not easy to read.
        int anotherVariable = 50; myVariable--; System.out.println("This is another one.");
    }
}
